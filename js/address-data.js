// 生成随机数的辅助函数
export function getRandomInt(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

// 从数组中随机选择一个元素
export function getRandomElement(array) {
    return array[Math.floor(Math.random() * array.length)];
}

// 生成随机街道号码
export function generateStreetNumber() {
    return getRandomInt(1, 9999).toString();
}

// 生成随机邮编
export function generatePostalCode(country) {
    const format = POSTAL_CODE_FORMATS[country.toLowerCase()] || '#####';
    return format.replace(/#/g, () => getRandomInt(0, 9).toString())
                .replace(/@/g, () => String.fromCharCode(65 + getRandomInt(0, 25)));
}

import { fileURLToPath } from 'url';
import { dirname } from 'path';
import path from 'path';
import fs from 'fs/promises';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// 获取国家数据
export async function getCountryData(country) {
    try {
        const countryCode = country.toLowerCase();
        const dataPath = path.join(__dirname, '..', 'data', countryCode);
        
        // 检查目录是否存在
        try {
            await fs.access(dataPath);
        } catch (error) {
            console.error(`数据目录不存在: ${dataPath}`);
            return null;
        }
        
        // 读取国家的所有数据文件
        const files = await fs.readdir(dataPath);
        const data = {};
        
        for (const file of files) {
            const name = path.basename(file, '.json');
            const content = await fs.readFile(path.join(dataPath, file), 'utf8');
            data[name] = JSON.parse(content);
        }
        
        return data;
    } catch (error) {
        console.error(`Error loading data for country ${country}:`, error);
        return null;
    }
}

// 获取地区/州/省列表
export async function getRegions(country) {
    const data = await getCountryData(country);
    if (!data) return [];

    // 根据不同国家返回相应的地区数据
    const regionTypes = ['states', 'provinces', 'regions', 'prefectures', 'districts'];
    for (const type of regionTypes) {
        if (data[type] && Array.isArray(data[type])) {
            return data[type];
        }
    }
    
    return [];
}

// 获取城市列表
export async function getCities(country) {
    const data = await getCountryData(country);
    if (!data) return [];

    // 首先检查是否有直接的城市列表
    if (data.cities && Array.isArray(data.cities)) {
        return data.cities;
    }

    // 如果没有直接的城市列表，从地区数据中收集所有城市
    const regions = await getRegions(country);
    if (!regions || !Array.isArray(regions)) return [];

    // 收集所有城市
    const allCities = new Set();
    regions.forEach(region => {
        if (region.cities && Array.isArray(region.cities)) {
            region.cities.forEach(city => allCities.add(city));
        }
        // 某些国家使用areas而不是cities
        if (region.areas && Array.isArray(region.areas)) {
            region.areas.forEach(area => allCities.add(area));
        }
    });

    return Array.from(allCities);
}

// 获取街道列表
export async function getStreets(country) {
    const data = await getCountryData(country);
    if (!data || !data.streets) return [];
    return data.streets;
}

